#!/bin/bash

NANORC_DIRS=('/usr/share/nano-syntax-highlighting/' '/usr/share/nano/')

# tested with Debian (all), RH(all), archlinux
for dir in ${NANORC_DIRS[@]}
do
   echo '##################### generated by enable-syntax.sh' >> ~/.nanorc
   find ${dir} -iname "*.nanorc" -exec echo include {} \; >> ~/.nanorc
done
